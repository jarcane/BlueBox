PROGRAM START
IGNORE  FACTORIAL SOLVER
IGNORE
PRINT "FACTORIAL SOLVER"
PRINT "PLEASE USE WHOLE NUMBERS"
SET #RUN 1
WHILE EQUALS #RUN 1
PROMPT # "ENTER NUMBER: "
SET #X &LAST
SET #Y #X
SET #TOTAL 1
WHILE GREATER #Y 0
MULT #TOTAL #Y
SET #TOTAL &LAST
SUB #Y 1
SET #Y &LAST
WHEND
PRINT #X "! = " #TOTAL
PROMPT $ "RUN AGAIN? "
IF EQUALS &LAST "YES"
IFY SET #RUN 1
IFN SET #RUN 0
WHEND
PROGRAM STOP
